#include <libft.h>

char	*ft_strdup(const char *s)
{
	int		i;
	int		str_len;
	char	*s_cpy;

	i = 0;
	str_len = ft_strlen(s) + 1;
	s_cpy = malloc(str_len * sizeof(char));
	if (!s_cpy)
		return (NULL);
	while (s[i])
	{
		s_cpy[i] = s[i];
		i++;
	}
	s_cpy[i] = '\0';
	return (s_cpy);
}
