#include <mathft.h>

t_vec3	vec3_sub(t_vec3 u, t_vec3 v)
{
	t_vec3	w;

	w.x = (u.x - v.x);
	w.y = (u.y - v.y);
	w.z = (u.z - v.z);
	return (w);
}
