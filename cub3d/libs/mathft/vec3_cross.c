#include <mathft.h>

t_vec3	vec3_cross(t_vec3 u, t_vec3 v)
{
	t_vec3	w;

	w.x = (u.y * v.z) - (u.z * v.y);
	w.y = (u.z * v.x) - (u.x * v.z);
	w.z = (u.x * v.y) - (u.y * v.x);
	return (w);
}
