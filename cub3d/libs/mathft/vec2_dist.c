#include <mathft.h>

double	vec2_dist(t_vec2 u, t_vec2 v)
{
	double	dist;

	dist = sqrt((u.x - v.x) * (u.x - v.x) + (u.y - v.y) * (u.y - v.y));
	return (dist);
}
