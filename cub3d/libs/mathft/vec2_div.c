#include <mathft.h>

t_vec2	vec2_div(t_vec2	u, long k)
{
	double	inv;
	t_vec2	w;

	if (k == 0)
		return (u);
	inv = 1.0 / k;
	w.x = u.x * inv;
	w.y = u.y * inv;
	return (w);
}
